/**
 * reset_to_98 - Updates the value of the integer pointed to by n to 98.
 * @n: A pointer to the integer to be updated.
 *
 * This function takes a pointer to an integer and sets the value it points
 * to 98, demonstrating the use of pointers in C.
 */
void reset_to_98(int *n)
{
*n = 98;
}
